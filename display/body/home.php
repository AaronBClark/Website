<h1>Software Engineering Club</h1>

<p>The Software Engineering Club at Moreno Valley College will
promote teamwork, teach industry standard collaboration techniques, and solidify the skills of
computer science students by hosting periodic software development marathons in which the 
goal will be to conceptualize, design, and produce a unique application as a group in a single</p>

<p>In addition, a variety of software development topics ranging from basic to advanced
will be discussed and demonstrated by qualified club members and/or advisors. These new 
skills will be put to the test in fun-filled group exercises and competitions.</p>

<p>All activities and applications will be documented on a club website as a showcase of the 
computer science program at Moreno Valley College.</p>
